package org.opentcs.util.math;

/**
 * Constants for bit masks commonly used.
 *
 * @author Stefan Walter (Fraunhofer IML)
 */
public interface BitMasks {

  /**
   * An integer with only bit 0 set.
   */
  int BIT_0 = 0x00000001;
  /**
   * An integer with only bit 1 set.
   */
  int BIT_1 = 0x00000002;
  /**
   * An integer with only bit 2 set.
   */
  int BIT_2 = 0x00000004;
  /**
   * An integer with only bit 3 set.
   */
  int BIT_3 = 0x00000008;
  /**
   * An integer with only bit 4 set.
   */
  int BIT_4 = 0x00000010;
  /**
   * An integer with only bit 5 set.
   */
  int BIT_5 = 0x00000020;
  /**
   * An integer with only bit 6 set.
   */
  int BIT_6 = 0x00000040;
  /**
   * An integer with only bit 7 set.
   */
  int BIT_7 = 0x00000080;
  /**
   * An integer with only bit 8 set.
   */
  int BIT_8 = 0x00000100;
  /**
   * An integer with only bit 9 set.
   */
  int BIT_9 = 0x00000200;
  /**
   * An integer with only bit 10 set.
   */
  int BIT_10 = 0x00000400;
  /**
   * An integer with only bit 11 set.
   */
  int BIT_11 = 0x00000800;
  /**
   * An integer with only bit 12 set.
   */
  int BIT_12 = 0x00001000;
  /**
   * An integer with only bit 13 set.
   */
  int BIT_13 = 0x00002000;
  /**
   * An integer with only bit 14 set.
   */
  int BIT_14 = 0x00004000;
  /**
   * An integer with only bit 15 set.
   */
  int BIT_15 = 0x00008000;
}
